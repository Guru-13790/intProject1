# Prompt the user to enter three integer numbers
num1 = int(input("Enter the first integer number: "))
num2 = int(input("Enter the second integer number: "))
num3 = int(input("Enter the third integer number: "))

# Calculate the sum of the numbers
sum_of_numbers = num1 + num2 + num3

# Calculate the product of the numbers
product_of_numbers = num1 * num2 * num3

# Calculate the average of the numbers
average_of_numbers = sum_of_numbers / 3

# Print the sum, product, and average
print("The sum of the numbers is:", sum_of_numbers)
print("The product of the numbers is:", product_of_numbers)
print("The average of the numbers is:", average_of_numbers)
